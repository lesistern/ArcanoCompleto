/**
 * Translates a single ability name from English to Spanish
 */
export function translateAbility(ability: string): string {
  const normalized = ability.toLowerCase().trim();
  return CLASS_ABILITY_TRANSLATIONS[normalized] || ability;
}

/**
 * Translates an array of abilities from English to Spanish
 */
export function translateAbilities(abilities: string[]): string[] {
  return abilities.map(translateAbility);
}

/**
 * Translates a comma-separated string of abilities
 */
export function translateAbilityString(abilitiesString: string): string {
  if (!abilitiesString) return '-';
  const abilities = abilitiesString.split(',').map(a => a.trim());
  return translateAbilities(abilities).join(', ');
}
